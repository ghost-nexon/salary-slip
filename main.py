def generate_html_with_deepseek():
    """Generate HTML email content using DeepSeek R1"""
    
    logger.info("📝 Sending request to DeepSeek R1 model...")
    
    prompt_message = f"""You are an expert email designer and motivational coach for SSC exam aspirants. 
Create a beautifully formatted HTML email about SSC ASO salary.

SALARY DATA:
{salary_data}

STRICT RULES:
1. Output ONLY valid HTML code. Do NOT use markdown like ```html or ```.
2. Use professional inline CSS with modern design (gradients, shadows, rounded corners).
3. Create a visually appealing email with:
   - Header with Government of India branding
   - Employee details section
   - Salary table with alternating row colors
   - Deductions section
   - Net salary highlight box
   - Motivational message with "Stop Dreaming. Start Preparing."
   - Call-to-action button for preparation
   - Benefits of SSC ASO job
   - Footer with date and motivational quote
4. Use colors: saffron (#FF9933), white (#FFFFFF), green (#138808) for Indian flag theme
5. Make it mobile-responsive
6. Add emojis for better visual appeal (💰, 📚, 🎯, 🏆, 💼)
7. Include current date: {datetime.now().strftime('%B %d, %Y')}
8. Add a section explaining why SSC ASO is a dream job
9. Add a note: "This email was generated by DeepSeek R1 AI for SSC aspirants"

The email should inspire SSC aspirants to work hard for this position.
"""

    try:
        # Call DeepSeek R1 model
        completion = client.chat.completions.create(
            model="deepseek-ai/DeepSeek-R1",
            messages=[
                {
                    "role": "system",
                    "content": "You are an expert HTML email designer specializing in government job exam motivation."
                },
                {
                    "role": "user",
                    "content": prompt_message
                }
            ],
            temperature=0.7,
            max_tokens=4000,
            timeout=60  # 60 seconds timeout
        )
        
        # Extract AI response
        ai_response = completion.choices[0].message.content
        
        # Remove DeepSeek's <think> tags if present
        html_content = re.sub(r'<think>.*?</think>', '', ai_response, flags=re.DOTALL).strip()
        
        # Remove markdown code blocks if AI wraps them
        html_content = html_content.replace('```html', '').replace('```', '').strip()
        
        # Save HTML for debugging
        with open('generated_email.html', 'w', encoding='utf-8') as f:
            f.write(html_content)
        
        logger.info("✅ HTML email generated successfully by DeepSeek R1!")
        return html_content
        
    except Exception as e:
        logger.error(f"⚠️ Error calling DeepSeek R1: {e}")
        logger.info("🔄 Using fallback HTML template...")
        return get_fallback_html()

