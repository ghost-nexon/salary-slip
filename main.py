def send_email(html_content, image_path='image.jpg'):
    """Send email with HTML content and optional image"""
    
    logger.info(f"📧 Preparing email to {TO_EMAIL}...")
    
    msg = MIMEMultipart('related')
    msg['From'] = EMAIL_ADDRESS
    msg['To'] = TO_EMAIL
    msg['Subject'] = f"💰 SSC ASO Salary {datetime.now().strftime('%d %b %Y')} - Your Dream Govt Job 🇮🇳"
    
    # Attach HTML content
    msg_alternative = MIMEMultipart('alternative')
    msg.attach(msg_alternative)
    msg_alternative.attach(MIMEText(html_content, 'html'))
    
    # Attach image if exists
    if os.path.exists(image_path):
        try:
            with open(image_path, 'rb') as img_file:
                img = MIMEImage(img_file.read(), name=os.path.basename(image_path))
                img.add_header('Content-ID', '<salary_image>')
                img.add_header('Content-Disposition', 'inline', filename=os.path.basename(image_path))
                msg.attach(img)
                logger.info(f"✅ Image '{image_path}' attached!")
        except Exception as e:
            logger.warning(f"⚠️ Could not attach image: {e}")
    else:
        logger.info(f"ℹ️ Image '{image_path}' not found, sending text-only email")
    
    # Send email
    try:
        logger.info("📤 Connecting to Gmail SMTP...")
        server = smtplib.SMTP('smtp.gmail.com', 587)
        server.starttls()
        server.login(EMAIL_ADDRESS, EMAIL_PASSWORD)
        server.send_message(msg)
        server.quit()
        
        logger.info("=" * 50)
        logger.info("✅ EMAIL SENT SUCCESSFULLY!")
        logger.info(f"📨 To: {TO_EMAIL}")
        logger.info(f"📋 Subject: SSC ASO Salary - {datetime.now().strftime('%d %b %Y')}")
        logger.info("=" * 50)
        return True
        
    except Exception as e:
        logger.error(f"❌ Failed to send email: {e}")
        return False

